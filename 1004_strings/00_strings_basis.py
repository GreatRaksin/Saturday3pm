'''Строки - последовательность символов, заключенная в кавычки'''

a = 'hello'  # <- строка - последовательность символов
print(type(a))
print(type(55))
print(type(23.5))

b = ['h', 'e', 'l', 'l', 'o']  # <- список - упорядоченный набор элементов
print('2' + '2')  # строчки можно складывать ТОЛЬКО со строчками | конкатенация
print('a' * 5)  # строчки можно умножать ТОЛЬКО на числа
print(a[3:])  # разрезать строчки можно

