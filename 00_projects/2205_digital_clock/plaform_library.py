import platform

print(platform.machine())  # информация о машине, на которой запускается код
print(platform.node())  # сетевое имя компьютера
print(platform.processor())  # имя процессора (внутреннее)
print(platform.system())  # информация о системе
print(platform.win32_ver())  # версия винды
print(platform.mac_ver())  # версия макоси